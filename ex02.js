//02- Escreva um programa Javascript para imprimir a soma de dois números.

A = 20;
B = 30;
Soma = A + B;
//operaçao de concatenação de string com valor numérico "+"

console.log("Soma: " + Soma);

//utilizando a palavra reservada "var"

var valor01 = 20;
var valor02 = 30;
var soma = (valor01 + valor02);

console.log("soma: " + soma);

// utilizando a palavra reservada "let"

let valor03 = 50;
let valor04 = 10;
let soma01 = (valor03 + valor04);

console.log("soma: " + soma01);

// utilizando a palavra reservada "const"

const valor05 = 40;
const valor06 = 60;
const soma02 = (valor05 + valor06);

console.log("soma: " + soma02);